// Boolean value is either true or false.
//     - Write three JavaScript statement example which provide truthy value.
//     - Write three JavaScript statement example which provide falsy value.


// Truthy value
if ("notEmpty"){
    console.log("True");
}
else{
    console.log("False");
}

if ("7"){
    console.log("True");
}
else{
    console.log("False");
}

if ([]){
    console.log("True");
}
else{
    console.log("False");
}

// falsy value
if (""){
    console.log("True");
}
else{
    console.log("False");
}

if (0){
    console.log("True");
}
else{
    console.log("False");
}

if (null){
    console.log("True");
}
else{
    console.log("False");
}